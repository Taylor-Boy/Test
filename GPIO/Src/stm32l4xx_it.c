/* USER CODE BEGIN 1 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	uint16_t Len;
	Len = strlen(USART1RECV);
	if(GPIO_Pin == GPIO_PIN_8)	//KEY2 亮蓝灯 打印当前接收数据缓冲区字长
	{
		LED_B_OFF;
		LED_G_ON;
		LED_R_ON;
		HAL_UART_Transmit(&huart2,"LD2001",6,1);
		printf("RX_BufLen = %d\r\n",Len);
	}
	if(GPIO_Pin == GPIO_PIN_9)	//KEY1 亮红灯 发送接收到的数据然后清除接收Buf
	{
		LED_B_ON;
		LED_G_ON;
		LED_R_OFF;
		//喂狗 溢出时间(Tout = pre*RLR / LSI) pre:4-256 RLR :0-4095 LSI:32KHz
		//HAL_IWDG_Refresh(&hiwdg);
		printf("%s\r\n",USART1RECV);
		USART_Clear(&huart1);
	}
	if(GPIO_Pin == GPIO_PIN_10)	//KEY0 亮绿灯 发送接收到的数据 不清除接收Buf
	{
		LED_B_ON;
		LED_G_OFF;
		LED_R_ON;
		printf("Len %d\r\n",Len);
		HAL_UART_Transmit(&huart1,(unsigned char*)USART1RECV,Len,1);
	}
}

